"""
Usando las propiedades de las cadenas o strings

Metodos de string
"""

mi_cadena = "Machine Learning con Python"

#String a mayúsculas: .upper()
print("Cadena a mayúsculas: {}".format(mi_cadena.upper()))

#String a minúsculas:
print("Cadena a mayúsculas: {}".format(mi_cadena.lower()))

#String a titulo:
print("Cadena a mayúsculas: {}".format(mi_cadena.title()))

#Solo la primera letra en mayúsculas:
print("String capitalizable: {}".format(mi_cadena.capitalize()))
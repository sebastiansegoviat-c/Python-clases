"""Listas en python"""

"""
Listas,
remove(): elimina un elemento de la lista indicando el valor  
"""

var1 = ["Python", "C++", "Javascript", "PHP", "Java", "Typescript"]
print("Los valores de mi lista son: {}".format(var1))

var1.remove("Python")
var1.remove("C++")

print("La lista actualizada es: {}".format(var1))

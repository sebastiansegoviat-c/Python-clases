"""Usando las propiedades de las cadenas o strings"""

"""Métodos de strings"""

mi_cadena = "Machine Learning con python"

"""String a mayúsculas: .upper()"""

print("Cadena a mayúsculas: {}".format(mi_cadena.upper()))

"""String a minúsculas: .lower()"""
print("Cadena a minúsculas: {}".format(mi_cadena.lower()))

"""A formato título: .title()"""
print("A formato título: {}".format(mi_cadena.title()))

"""Solo la primera letra en mayúsc. de la cadena"""
print("String capializable: {}".format(mi_cadena.capitalize()))

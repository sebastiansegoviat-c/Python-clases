"""Usando las propiedades de las cadenas o strings"""

cadena = "Lenguaje de programación en Python"

print("Tamaño del string: {}".format(len(cadena)))

print("Primera palabra: {}".format(cadena[0:8]))

print("El último caracter es: {} y el penúltimo caracter es: {}".format(cadena[-1], cadena[-2]))

print("Última palabra: {}".format(cadena[-6:]))

